#include <stdio.h>

int main(void)
{
    // Float - %f
    float f = 456.123f;
    printf("Float - %f\n", f);

    // Double -- %lf
    double d = 47789.56;
    printf("Double - %lf\n", d);

    // Long Double - %Lf
    long double ld = 7854554132.565;
    printf("Long Double - %Lf\n", ld);
    return 0;
}